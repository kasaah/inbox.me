import java.util.*;
public class NameInitials {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        String firstname= "Ebenezer";
        String middlename = "Amissah";
        String lastname = "Saah";
        
        char firstinitial = 'E';
        char middleinitial = 'A';
        char lastinitial = 'S';
        
        System.out.println(firstname + "\n"+ middlename + "\n" + lastname + "\n" + firstinitial +"." + middleinitial +"." + lastinitial);
        
    }
    
}
